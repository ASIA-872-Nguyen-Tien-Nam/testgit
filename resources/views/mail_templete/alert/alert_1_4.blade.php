期限までに一次評価が実施されていません。速やかにすべての被評価者の一次評価を実施してください。

【一次評価の流れ】
①ミライクの画面右の「評価対象シート一覧」でピンクで表示されている社員をクリック
また、画面左の「人事評価」→「人事評価シート一覧」でも一次評価ができます
②被評価者全員の評価が完了すると、「人事評価」→「総合評価」より被被評価者全員の評価結果を確認し（必要に応じて評価を変更できます）、「社員番号」の左の「✔」を選択し「確定」してください
③確定することで、一次評価が完了し、次の評価者へ移ります
④なお、一次評価結果を変更する場合は「確定解除」して、再度、評価してください（すでに次の評価者が評価を実施している場合は確定解除できませんので、その場合は、次の評価者へ確認してください）