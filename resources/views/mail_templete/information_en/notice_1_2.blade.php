Please Approve/Decline Objectives after having Objective Setting Interview with Employee.

【Objective Approval Process】

①Fill in Employee Interview content in "Interview Record" and "Register".
※Possible to "Register" leaving out Interview content.

②Click either "Approve" or "Decline" for Objective.

③When Declining objective, please approve Objective once employee has re-registered Objective until First Evaluation is initiated, it is possible to "Decline" Objective.

※Through MIRAIC Settings, this e-mail may be sent to both Evaluator and Employee.