【First Evaluation Process】
①Click on employee highlighted pink in "Evaluation Sheet List".
You can also initiate First Evaluation from "HR Evaluation" → "HR Evaluation Sheet List".

②Once all Employee Evaluation is complete, check all employee's Evaluation result through "HR Evaluation" → "Overall Evaluation" (Evaluation can be changed if necessary) , and check "✔" left of "Employee Number" and "Confirm".

③By confirming, First Evaluation will be completed and will transfer to next Evaluator. 

④If you wish to change First Evaluation result, click "Cancel Confirmation" and re-evaluate.
(you can not "Cancel Confirmation" if next Evaluator is already evaluating. When so, please check with next Evaluator)